import prisma from "../db/db.prisam.js";
import { ValidationError } from "../middlewares/errorHandler/index.js";

// 1. Get Top Performing Electricians (Leaderboard)
export const getTopElectricians = async (req, res, next) => {
    try {
        // Find electricians with the highest wallet balance or most referred orders
        const topElectricians = await prisma.electricianCustomer.findMany({
            where: { is_active: true },
            take: 5,
            orderBy: { wallet_balance: 'desc' }, // Ranking by highest earnings
            select: {
                id: true,
                name: true,
                phone_number: true,
                wallet_balance: true,
                _count: {
                    select: { referral_orders: true, personal_orders: true }
                }
            }
        });

        res.status(200).json({ success: true, topElectricians });
    } catch (error) {
        next(error);
    }
};

// 2. Get All Electricians (For the Table)
export const getAllElectriciansAdmin = async (req, res, next) => {
    try {
        const { search, kyc_status, page = 1 } = req.query;
        const limit = 10;
        
        const whereClause = {};
        if (kyc_status && kyc_status !== 'ALL') whereClause.kyc_stat = kyc_status;
        if (search) {
            whereClause.OR = [
                { name: { contains: search, mode: 'insensitive' } },
                { phone_number: { contains: search } },
                { referral_code: { contains: search, mode: 'insensitive' } }
            ];
        }

        const totalItems = await prisma.electricianCustomer.count({ where: whereClause });

        const electricians = await prisma.electricianCustomer.findMany({
            where: whereClause,
            take: limit,
            skip: (parseInt(page) - 1) * limit,
            orderBy: { id: 'desc' },
            include: {
                _count: { select: { referral_orders: true, payoutRequests: true } }
            }
        });

        res.status(200).json({
            success: true,
            totalItems,
            totalPages: Math.ceil(totalItems / limit),
            currentPage: parseInt(page),
            electricians
        });
    } catch (error) {
        next(error);
    }
};

// 3. Get Deep Details for the Modal (Wallet Ledger, Payouts, Orders)
export const getElectricianDetails = async (req, res, next) => {
    try {
        const { id } = req.params;

        const electrician = await prisma.electricianCustomer.findUnique({
            where: { id: parseInt(id) },
            include: {
                addresses: true,
                walletLedgers: {
                    orderBy: { created_at: 'desc' },
                    take: 20 // Last 20 transactions
                },
                payoutRequests: {
                    orderBy: { id: 'desc' },
                    take: 5
                },
                // Aggregate total sales they brought in
                referral_orders: {
                    select: { total_amount: true, status: true },
                    where: { status: 'DELIVERED' }
                }
            }
        });

        if (!electrician) return next(new ValidationError("Electrician not found"));

        // Calculate total revenue generated by this electrician
        const totalRevenueGenerated = electrician.referral_orders.reduce((sum, order) => sum + Number(order.total_amount), 0);

        res.status(200).json({
            success: true,
            electrician: {
                ...electrician,
                totalRevenueGenerated
            }
        });
    } catch (error) {
        next(error);
    }
};

// 4. Process Payout Request (Admin sends money to Bank Account)
export const processPayout = async (req, res, next) => {
    try {
        const { requestId } = req.params;
        const { action, bank_utr } = req.body; // action = "APPROVE" or "REJECT"

        const request = await prisma.payoutRequest.findUnique({ where: { id: parseInt(requestId) }});
        if (!request || request.status !== "REQUESTED") return next(new ValidationError("Invalid Payout Request"));

        await prisma.$transaction(async (tx) => {
            if (action === "APPROVE") {
                if (!bank_utr) throw new Error("Bank UTR / Transaction ID is required to approve");
                
                // Update Payout Status
                await tx.payoutRequest.update({
                    where: { id: request.id },
                    data: { status: "PROCESSED", bank_utr, processed_at: new Date() }
                });

                // Deduct from Wallet
                await tx.electricianCustomer.update({
                    where: { id: request.electrician_id },
                    data: { wallet_balance: { decrement: request.amount } }
                });

                // Add to Ledger
                await tx.walletLedger.create({
                    data: {
                        electrician_id: request.electrician_id,
                        type: "DEBIT",
                        category: "WITHDRAWAL",
                        amount: request.amount,
                        reference_id: bank_utr,
                        description: "Wallet Withdrawal to Bank Account"
                    }
                });
            } else {
                // Reject: Just update status, money stays in wallet
                await tx.payoutRequest.update({
                    where: { id: request.id },
                    data: { status: "FAILED", processed_at: new Date() }
                });
            }
        });

        res.status(200).json({ success: true, message: `Payout ${action}D successfully.` });
    } catch (error) {
        next(error);
    }
};